%% Solid iron enthaply (J/mol)
function cp_Fe = cpFe(Tp)
%% Coefficients for [cp & del_h] of Solid Iron(Fe)
A0 = 18.42868 ;
A1 = 24.64301 ;
A2 = -8.913720 ;
A3 = 9.664706 ;
A4 = -0.012643 ;
A5 = -6.573022 ;
A6 = 0 ;
B0 = -57767.65 ;
B1 = 137919.7 ;
B2 = -122773.2 ;
B3 = 38682.42 ;
B4 = 3993.080 ;
B5 = 24078.67 ;
B6 = 141e-3 ;
C0 = -325.8859 ;
C1 = 28.92876 ;
C2 = 0.000000 ;
C3 = 0.000000 ;
C4 = 411.9629 ;
C5 = 745.8231 ;
C6 = -71e-3 ;
D0 = -776.7387 ;
D1 = 919.4005 ;
D2 = -383.7184 ;
D3 = 57.08148 ;
D4 = 242.1369 ;
D5 = 697.6234 ;
D6 = -436e-3 ;

%% Calculate cp [J/mol/K]
if (Tp <= 700)
    cp_Fe = A0 + A1*(Tp/1000) ...                 % [J/(mol*K)]
        + A2*(Tp/1000)^2 ...
        + A3*(Tp/1000)^3 ...
        + A4/(Tp/1000)^2 ;
    
elseif (Tp > 700) && (Tp <= 1042)
    cp_Fe = B0 + B1*(Tp/1000) ...                 % [J/(mol*K)]
        + B2*(Tp/1000)^2 ...
        + B3*(Tp/1000)^3 ...
        + B4/(Tp/1000)^2 ;
    
elseif (Tp > 1042) && (Tp <= 1100)
    cp_Fe = C0 + C1*(Tp/1000) ...                 % [J/(mol*K)]
        + C2*(Tp/1000)^2 ...
        + C3*(Tp/1000)^3 ...
        + C4/(Tp/1000)^2 ;
    
else % if (Tp > 1100) && (Tp <= 1809)
    cp_Fe = D0 + D1*(Tp/1000) ...                 % [J/(mol*K)]
        + D2*(Tp/1000)^2 ...
        + D3*(Tp/1000)^3 ...
        + D4/(Tp/1000)^2 ;
    
end

return